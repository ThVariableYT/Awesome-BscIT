# Lists
# Creating Lists

# Empty Lists
empty_list = []
print("Empty list:", empty_list)                            # Output: []

# List with initial values
numbers = [1, 2, 3, 4, 5]
print("List of numbers:", numbers)                          # Output: [1, 2, 3, 4, 5]

# List with mixed data types
mixed_list = [1, "hello", 3.14, True, [1, 2]]
print("Mixed list:", mixed_list)                            # Output: [1, 'hello', 3.14, True, [1, 2]]

# Using list() constructor
constructed_list = list((1, 2, 3))
print("Constructed list:", constructed_list)                # Output: [1, 2, 3]

